/**
 * The constructs supported within a Automaton construct.
 *  
 * @author amit.das@cloudbyte.com
 *
 */
enum AutomatonConstructType {

    /*
     * Property to map automaton.
     */
    automaton,
    /*
     * Property to map job.
     */
    job,
    /*
     * Property to map connection.
     */
    conn,
    /*
     * Property to map settings.
     */
    settings
}
